with open(output_path, "w", encoding="utf-8") as f:
    f.write("\n".join(sql_lines))
    f.write("\n")

print(f"\nSaved: {output_path}")
print(f"Total SQL statements: {len([l for l in sql_lines if l.startswith('INSERT')])}")
