def sql_str(val):
    """Wrap a string value for SQL. Returns NULL for None."""
    if val is None:
        return "NULL"
    escaped = str(val).replace("'", "''")
    return f"'{escaped}'"

def sql_num(val):
    """Numeric literal or NULL."""
    if val is None:
        return "NULL"
    return str(val)

def sql_date(val):
    """Date literal or NULL. Accepts date objects or strings."""
    if val is None:
        return "NULL"
    if isinstance(val, datetime.date):
        return f"'{val.isoformat()}'"
    return f"'{val}'"

def sql_ts(val):
    """Timestamp literal or NULL."""
    if val is None:
        return "NULL"
    if isinstance(val, datetime.datetime):
        return f"'{val.strftime('%Y-%m-%d %H:%M:%S')}'"
    if isinstance(val, datetime.date):
        return f"'{val.isoformat()} 00:00:00'"
    return f"'{val}'"

