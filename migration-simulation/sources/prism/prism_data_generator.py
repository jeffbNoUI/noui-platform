def rand_date(start_yr, end_yr):
    if start_yr > end_yr:
        end_yr = start_yr
    start = datetime.date(start_yr, 1, 1)
    end = datetime.date(end_yr, 12, 31)
    delta = (end - start).days
    if delta <= 0:
        return start
    return start + datetime.timedelta(days=random.randint(0, delta))

def format_birth_dt(dt, orig_sys):
    if orig_sys == "OPUS" and random.random() < BIRTH_DT_ERROR_RATE:
        return f"{dt.month:02d}{dt.day:02d}{dt.year:04d}"  # wrong: MMDDYYYY
    return dt.strftime("%Y%m%d")

def format_ssn(s, hire_dt):
    return f"{s[:3]}-{s[3:5]}-{s[5:]}" if hire_dt < SSN_BOUNDARY else s

def gen_ssn():
    return str(random.randint(100000000, 999999999))

